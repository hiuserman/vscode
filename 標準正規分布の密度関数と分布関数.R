par(mai= c(1,1,1,1))
par(family = "HiraKakuProN-W3")
x<-seq(-4,4,0.1)
x2<-seq(-2,1,0.1)
plot(x,dnorm(x,0,1),type="l",col="red",ylim=c(0,0.4),lwd=2,ylab="密度関数",main="標準正規分布の密度関数と分布関数")
par(new=T)
plot(x2,dnorm(x2,0,1),axes=F,type="h",col="red",xlim=c(-4,4),ylim=c(0,0.4),lwd=2,xlab="",ylab="")
abline(v=-2,col="red",lty=2,xlim=c(-4,4))
abline(v=1,col="red",lty=2,xlim=c(-4,4))
par(new=T)
plot(x,pnorm(x,0,1),axes=F,type="l",col="green",ylim=c(0,1),lwd=2,xlab="",ylab="")
axis(4)
mtext("分布関数",side=4,line=3)
text(x=-3,y=0.7,"x=-2~1")
p<-pnorm(1,0,1)-pnorm(-2,0,1)
text(x=-3,y=0.5,sprintf("p=%.3f",p))
